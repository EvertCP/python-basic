# Imprimimos un texto plano
print("Python has three numeric types: int, float, and complex")

# Creamos una variable que se llama myValue y dentro de ella guardamos el valor de 1
myValue = 1

#Imprmimos el valor de myValue
print(myValue)

# Imprimimos el tipo de la variable myValue
print(type(myValue))

# Concatenamos la variable myValue, con texto y el tipo de variable, y lo imprimimos
print(str(myValue) + " is of the data type " + str(type(myValue)))

# Cambiamos el valor de la variable myValue - En Python podemos cambiar el valor de las variables
myValue = 3.14

# Volvemos a imprimir la variable, el tipo y la concatenacion de las variables
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))

# Volvemos a cambiar el valor de la variable pero ahora por un numero complejo (complex)
myValue = 5j

# Volvemos a imprimir la variable, el tipo y la concatenacion de las variables
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))

# Volvemos a cambiar el valor de la variable pero ahora por un boolean
